package JAVA.operators;

public class unary {
    public static void main(String[] args) {
        int a = 10, b = 5;

        // Postfix operation
        System.out.println("a increament: " + a++);
        System.out.println("a decreamenet: " + a--);

        // Prefix operation
        System.out.println("b increament: " + ++b);
        System.out.println("b decreament: " + --b);
    }
}
